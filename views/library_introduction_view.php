		<h1>Introduction to Morzo</h1>
		<p>
			The Morzo project is a roleplaying engine.
			There is no graphics, no prescripted quests, no buildings at the start of the game.
			The players provide all the action.
		</p>
		<p>
			What the game does provide is a world with resources and ways to use them.
			You explore the possibilities and make your own adventures.
		</p>
		<h2>The user page</h2>
		<p>
			When you first log in you don't have any characters.
			You can have press New Actor to get one. All your actor are always "online". The game doesn't stop when you log out.
			The things you do in game take a long time and your actors will keep working while you do other things.
			You will notice that your actor has no name at first. When you enter an actor you can rename it by clicking on the name text.
		</p>
		<h2>Events</h2>
		<p>
			The first tab shows all events that have happened. You will see other actors doing things and they will all be unnamed for you at first.
			You have to ask them for their names or make something up if they don't tell you.
		</p>
		<h2>Locations</h2>
		<p>
			Locations show all places you can go to. These will also be unnamed at first and you can name them the same way as you do with actors.
		</p>
		<h2>People</h2>
		<p>
			This page lists all the actors at your location. Along with some actions.
			You can point at a person, whisper a private message or try to hurt them with an attack.
		</p>
		<h2>Resources</h2>
		<p>
			On this tab you can see what natural resources are available.
			By clicking on a resource name, you can see the different ways to harvest the resource.
			You can also start a hunt from here.
		</p>
		<h2>Projects</h2>
		<p>
			This tab lists all projects and hunts that have been started.
			You can join any of them to get stuff done.
			If you click on a project or hunt name you can see the details, supply resources or cancel a project.
		</p>
		<h2>Inventory</h2>
		<p>
			On this tab you can manage your stuff. Enter the amounts you want to move and click Transfer here at the desired designation.
			There is currently no way to give stuff to other actors directly so you need to put it on the ground or in containers and hope nobody steals your stuff.
		</p>
